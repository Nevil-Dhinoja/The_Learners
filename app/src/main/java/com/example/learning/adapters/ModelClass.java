package com.example.learning.adapters;

public class ModelClass {
    private String courseName;
    private String courseCat;
    private int courseImage;

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public String getCourseCat() {
        return courseCat;
    }

    public void setCourseCat(String courseCat) {
        this.courseCat = courseCat;
    }

    public int getCourseImage() {
        return courseImage;
    }

    public void setCourseImage(int courseImage) {
        this.courseImage = courseImage;
    }
}
